using System;

namespace Loops
{
    /// <summary>
    ///         
    /// </summary>
    internal class Program
    {
        static void Main(string[] args)
        {
            for (int i = 0; i < 20; i+=1)
            {
                Console.WriteLine(i);
            }
            Console.WriteLine("");

            for (int i = 1; i < 20; i+=2)
            {
                Console.WriteLine(i);
            }

        }
    }
}
